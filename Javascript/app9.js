"use strict"

//Operadores de Asignación


var datoA = 10;


//Asignación simple
var igual = datoA;

console.log("El resultado de la asginación simple es: "+ igual);
//a la variable se le asigna un valor 


//Sumar y Asignar
  var masIgual=10;
  masIgual += datoA;
  console.log("El resultado de la asginación += es: "+ masIgual);
//se suma el valor por el mismo valor en este caso

//Restar y asignar
var menosIgual=10;
menosIgual -= datoA;
console.log("El resultado de la asignación -= es: "+ menosIgual);
//se resta el valor por el valor 

//Multiplicar y asignar

var porIgual= 10;
porIgual *= datoA;
console.log("El resultado de la asignación *= es: "+ porIgual);
//se multiplico el mismo numero por su mismo valor 10*10

//dividir y asignar

var entreIgual=20;
entreIgual /= datoA;
console.log("El resultado de la asignación /= es: "+ entreIgual);
//se divide el valor por que anterior valor, 20/10 = 2 


